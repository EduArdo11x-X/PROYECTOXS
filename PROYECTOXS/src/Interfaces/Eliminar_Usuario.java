    public void MostrarDatos(ObjectSet result) {
        String matrizUsuario[][] = new String[result.size()][7];
        int matrizUsuario1[][] = new int [result.size()][8];

        if (result.size() == 0) {
            JOptionPane.showMessageDialog(null, "ElUsuario no se encuentra en la base de datos");
        } else {
            for (int i = 0; i < result.size(); i++) {

                Persona miE = new Persona();

                miE = (Persona) result.get(i);
                matrizUsuario[i][0]= miE.getCedula();
                matrizUsuario[i][1] = miE.getPrimer_nom();
                matrizUsuario[i][2]= miE.getSegundo_Nom();
                matrizUsuario[i][3]= miE.getPrimer_Ape();
                matrizUsuario[i][4] = miE.getSegundo_Ape();
                matrizUsuario[i][5]= miE.getCalle_principal();
                matrizUsuario[i][6]=miE.getCalle_Secundaria();
                matrizUsuario[i][7]= miE.getSexo();
                matrizUsuario1[i][8]= miE.getEdad();
                

                jTable1.setModel(new javax.swing.table.DefaultTableModel(matrizUsuario, new String[]{"Cedula","Primer_nom" ,"Segundo_Nom","Primer_Ape","Segundo_Ape","Calle_principal","Calle_Secundaria" ," Sexo","Edad"}));

            }
        }

    }
    

    private void Eliminar_UsuarioCedula(ObjectContainer BaseD) {
       Registro_UsuarioN Einterfaz = new Registro_UsuarioN();//Crear un objeto de la clase Estudiantes para traer el metodo Comprobar_Estudiantes

        if (jTextField1.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Codigo no valido");
        } else {

            String Cedula = jTextField1.getText();
            Persona Leliminar = new Persona(Cedula, null, null, null, null, null, null , null, 0);
            ObjectSet result = BaseD.get(Leliminar);

            if (Einterfaz.Comprobar_Usuario(BaseD, Cedula) == 0) {

                JOptionPane.showMessageDialog(null, "El Usaurio no existe en la base de datos");

            } else {
                Persona Estudianteeliminar = (Persona) result.next();

                BaseD.delete(Estudianteeliminar);
                JOptionPane.showMessageDialog(null, "El Usuario fue eliminado de la base de datos exitosamente");
            }

        }

        //Borrar el campo de texto
        jTextField1.setText("");
    }
